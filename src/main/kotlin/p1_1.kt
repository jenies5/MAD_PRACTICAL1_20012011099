fun main() {

    // Defining Variables :
    val a: Int = 120
    val b: Double = 34.546
    val c: Float = 12.34f
    val d: Long = 1233443556
    val e: Short = 34
    val f: Byte = 89
    val g: Char = 'a'
    val done: Boolean = false
    val name: String = "Jenies"

    // Printing all the Variables :
    println("Integer Value : $a")
    println("Double Value : $b")
    println("Float Value : $c")
    println("Long Value : $d")
    println("Short Value : $e")
    println("Byte Value : $f")
    println("Char Value : $g")
    println("Boolean Value : $done")
    println("String Value : $name")
}